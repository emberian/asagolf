        // df-acong unfolds BOTH sides to ( EQ /\ SGN ); EQ is an equation
        // whose two sides are exactly swapped between the two ACong
        // orderings, and SGN = 0 <_ ( D1 * D2 ) ; the swapped SGN is
        // 0 <_ ( D2 * D1 ) — bridged by of-mulcom.  We unfold the given,
        // commute the SGN product, re-pack, and bi_rev the swapped df-acong.
        let (o, q, ee, ff) = (lf("o"), lf("q"), lf("e"), lf("f"));
        let (pp, aa) = (lf("p"), lf("a"));
