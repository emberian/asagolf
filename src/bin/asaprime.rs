        //   pm3.2 |- ( ph -> ( ps -> ( ph /\ ps ) ) )  — two ax-mp steps.
        let pm32 = el0
            .app("pm3.2", &[("ph", eqc_sw.clone()), ("ps", sgn_sw.clone())], &[])
            .unwrap(); // |- ( EQ' -> ( SGN' -> ( EQ' /\ SGN' ) ) )
