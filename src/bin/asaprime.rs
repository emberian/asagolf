        //     = ( ( Daef * Daef ) * ( ( sqd o p ) * ( sqd o q ) ) )
        let eq_l = mu(
            mu(dopq.clone(), dopq.clone()),
            mu(sqd0(aa.clone(), ee.clone()), sqd0(aa.clone(), ff.clone())),
        );
        let eq_r = mu(
            mu(daef.clone(), daef.clone()),
            mu(sqd0(o.clone(), pp.clone()), sqd0(o.clone(), q.clone())),
        );
        let eqc = eq0(eq_l.clone(), eq_r.clone());
        // EQ' is the swapped equation : eq_r = eq_l  (eqcom of EQ).
        let eqc_sw = eq0(eq_r.clone(), eq_l.clone());
        // SGN : 0 <_ ( Dopq * Daef ) ;  SGN' : 0 <_ ( Daef * Dopq ).
        let sgn = le0(z0(), mu(dopq.clone(), daef.clone()));
        let sgn_sw = le0(z0(), mu(daef.clone(), dopq.clone()));
        // Extract EQ and SGN from body_g via simpl/simpr (theorems
        //   simpl |- ( ( ph /\ ps ) -> ph ) ,  simpr |- ( ( ph /\ ps ) -> ps )
        // discharged by ax-mp against body_g = |- ( EQ /\ SGN )).
        let conj_eq_sgn = wa0(eqc.clone(), sgn.clone());
        let simpl_t = el0
            .app("simpl", &[("ph", eqc.clone()), ("ps", sgn.clone())], &[])
            .unwrap(); // |- ( ( EQ /\ SGN ) -> EQ )
        let eq_pf = mp0(conj_eq_sgn.clone(), eqc.clone(), body_g.clone(), simpl_t); // |- EQ
        let simpr_t = el0
            .app("simpr", &[("ph", eqc.clone()), ("ps", sgn.clone())], &[])
            .unwrap(); // |- ( ( EQ /\ SGN ) -> SGN )
        let sgn_pf = mp0(conj_eq_sgn.clone(), sgn.clone(), body_g.clone(), simpr_t); // |- SGN
        // EQ' = eqcom EQ.
        let eqcom_ax = el0
            .app("eqcom", &[("x", eq_l.clone()), ("y", eq_r.clone())], &[])
            .unwrap();
        let eq_sw_pf = mp0(eqc.clone(), eqc_sw.clone(), eq_pf.clone(), eqcom_ax);
