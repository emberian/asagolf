//!
//!  Construct  Cp = ( cp a c ( sqd e g ) )  — the ruler point on ray a->c
//!  at squared-distance ( sqd e g ) from a.
//!
//!   s1  G1a-rulerr  ( Ray a c Cp )                          [REAL $p]  ✔
//!   s2  G1b-rulerd  ( sqd a Cp ) = ( sqd e g )              [REAL $p]  ✔
